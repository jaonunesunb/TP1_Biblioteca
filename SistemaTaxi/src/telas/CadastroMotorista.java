public void carregarDadosTabelaMotorista() {
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"ID", "Nome", "Telefone", "CNH"}, 0);
        
        for (int i = 0; i < listaMotoristas.size(); i++) {
            Object linha[] = new Object[] {listaMotoristas.get(i).getId(),
                                           listaMotoristas.get(i).getNome_motorista(),
                                           listaMotoristas.get(i).getTelefone_motorista(),
                                           listaMotoristas.get(i).getCnh()};
                                           
            modelo.addRow(linha);
                    }
        tblMotoristas.setModel(modelo);
    }
