    private void btnSalvarDadosMotoristaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarDadosMotoristaActionPerformed
        if (txtIDMotorista.getText().equals("" ) || txtNomeMotorista.getText().equals("") || 
                txtTelefoneMotorista.getText().equals("") || 
                txtCNHMotorista.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
            
        }
        
        else {
             // get de dados
            int id = Integer.parseInt(txtIDMotorista.getText());
            String nome = txtNomeMotorista.getText();
            String telefone = txtTelefoneMotorista.getText();
            String cnh = txtCNHMotorista.getText();

            Motorista motorista = new Motorista(id, nome, telefone, cnh);
            
            listaMotoristas.add(motorista);
            
            carregarDadosTabelaMotorista();

            JOptionPane.showMessageDialog(null, "Passageiro cadastrado com sucesso", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
            
                // habilitar ou desabilitar bot천es
                btnNovoMotorista.setEnabled(true);
                btnSalvarDadosMotorista.setEnabled(false);
                btnCancelarMotorista.setEnabled(false);
                btnEditarMotorista.setEnabled(false);
                btnExcluirDadosMotorista.setEnabled(false);
                btnPesquisarMotorista.setEnabled(true);
                btnOkMotorista.setEnabled(false);

                 // habilitar ou desabilitar texto
                 txtIDMotorista.setEnabled(false);
                 txtNomeMotorista.setEnabled(false);
                 txtTelefoneMotorista.setEnabled(false);
                 txtCNHMotorista.setEnabled(false);

                 // limpar campos
                 txtIDMotorista.setText("");
                 txtNomeMotorista.setText("");
                 txtTelefoneMotorista.setText("");
                 txtCNHMotorista.setText("");
        }
    }//GEN-LAST:event_btnSalvarDadosMotoristaActionPerformed

